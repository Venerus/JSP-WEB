package com.test.dat.festival;

public class FestivalDTO {

	private String seq;
	private String tourAreaSeq;
	private String name;
	private String startDate;
	private String endDate;
	private String detail;
	private String address;
	
	private String lng;
	private String lat;
	
	private String festivalSeq;
	private String pic;

	
	public String getLng() {
		return lng;
	}


	public void setLng(String lng) {
		this.lng = lng;
	}


	public String getLat() {
		return lat;
	}


	public void setLat(String lat) {
		this.lat = lat;
	}

	public String getSeq() {
		return seq;
	}


	public void setSeq(String seq) {
		this.seq = seq;
	}


	public String getTourAreaSeq() {
		return tourAreaSeq;
	}


	public void setTourAreaSeq(String tourAreaSeq) {
		this.tourAreaSeq = tourAreaSeq;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getStartDate() {
		return startDate;
	}


	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}


	public String getEndDate() {
		return endDate;
	}


	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}


	public String getDetail() {
		return detail;
	}


	public void setDetail(String detail) {
		this.detail = detail;
	}


	public String getAddress() {
		return address;
	}


	public void setAddress(String address) {
		this.address = address;
	}


	public String getPic() {
		return pic;
	}


	public void setPic(String pic) {
		this.pic = pic;
	}


	public String getFestivalSeq() {
		return festivalSeq;
	}


	public void setFestivalSeq(String festivalSeq) {
		this.festivalSeq = festivalSeq;
	}
	
	

	
}
